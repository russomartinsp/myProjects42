char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	o;

	i = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	o = 0;
	while (src[o] != '\0' )
	{
		dest[i + o] = src[o];
		o++;
	}
	return (dest);
}
